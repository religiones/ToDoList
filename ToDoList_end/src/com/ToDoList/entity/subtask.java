package com.ToDoList.entity;

public class subtask {
	private String subtask_name;
	private int subtask_flag;
	
	public void Setsubtask_name(String subtask_name) {
		this.subtask_name = subtask_name;
	}
	
	public String Getsubtask_name() {
		return subtask_name;
	}
	
	public void Setsubtask_flag(int subtask_flag) {
		this.subtask_flag = subtask_flag;
	}
	
	public int Getsubtask_flag() {
		return subtask_flag;
	}
}
